$(document).ready(function() {
  $("h1").click(function() {
    alert("this is a header.");
  });

  $("h2").click(function() {
    alert("this is a second header.")
  });

  $("p").dblclick(function() {
    alert("this is a paragraph.");
  });

  $("img").click(function() {
    alert("this is an image.");
  });


});
